int		ft_strisempty(char *str)
{
	int		cmp;

	cmp = 0;
	while (str[cmp] != '\0')
	{
		if (str[cmp] != ' ' && str[cmp] != '\t' && str[cmp] != '\n')
			return (1);
		++cmp;
	}
	return (0);
}
