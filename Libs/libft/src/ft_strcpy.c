char		*ft_strcpy(char *dest, char *src)
{
	int c_a;

	c_a = 0;
	while (src[c_a] != '\0')
	{
		dest[c_a] = src[c_a];
		c_a++;
	}
	dest[c_a] = '\0';
	return (dest);
}
