#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		strlen;

	if (s != NULL && f != NULL)
	{
		strlen = ft_strlen(s);
		while (strlen--)
		{
			(*f)(s);
			s++;
		}
	}
}
